def draw_horizon(draw, x, y, width, scale=1):
    """Draw horizon/ground line."""
    for i in range(width):
        draw_pixel(draw, x + i, y, scale)

    import random
    random.seed(42)
    for i in range(0, width, 3):
        if random.random() > 0.7:
            draw_pixel(draw, x + i, y + 2 * scale, scale)
        if random.random() > 0.8:
            draw_pixel(draw, x + i, y + 4 * scale, scale)
            draw_pixel(draw, x + i + 1, y + 4 * scale, scale)


