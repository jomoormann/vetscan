# Colors
TRANSPARENT = (0, 0, 0, 0)
BLACK = (83, 83, 83, 255)  # Game's dark gray color


def draw_pixel(draw, x, y, scale=1):
    """Draw a single pixel (or scaled pixel block)."""
    if scale == 1:
        draw.point((x, y), fill=BLACK)
    else:
        draw.rectangle([x, y, x + scale - 1, y + scale - 1], fill=BLACK)


def draw_pixels(draw, base_x, base_y, pixels, scale=1):
    """Draw multiple pixels from a list of (x, y) offsets."""
    for px, py in pixels:
        if px >= 0 and py >= 0:  # Skip negative coordinates
            draw_pixel(draw, base_x + px * scale, base_y + py * scale, scale)


