def draw_pixel(draw, x, y, scale=1, color=BLACK):
    """Draw a single pixel (or scaled pixel block)."""
    if scale == 1:
        draw.point((x, y), fill=color)
    else:
        draw.rectangle([x, y, x + scale - 1, y + scale - 1], fill=color)


def draw_pixels(draw, base_x, base_y, pixels, scale=1, color=BLACK):
    """Draw multiple pixels from a list of (x, y) offsets."""
    for px, py in pixels:
        if px >= 0 and py >= 0:
            draw_pixel(draw, base_x + px * scale, base_y + py * scale, scale, color)


